def check_password_strength(password: str) -> None:
    # Criteria checks
    length_error = len(password) < 8
    lowercase_error = re.search(r"[a-z]", password) is None
    uppercase_error = re.search(r"[A-Z]", password) is None
    digit_error = re.search(r"\d", password) is None
    special_error = re.search(r"[!@#$%^&*(),.?\":{}|<>]", password) is None

    # Collect feedback
    comments = []
    if length_error:
        comments.append("[red]‚ö† Too short (use at least 8 characters).[/red]")
    if lowercase_error:
        comments.append("[red]‚ö† Missing lowercase letter.[/red]")
    if uppercase_error:
        comments.append("[red]‚ö† Missing uppercase letter.[/red]")
    if digit_error:
        comments.append("[red]‚ö† Missing a number.[/red]")
    if special_error:
        comments.append("[red]‚ö† Missing a special character (e.g., ! @ # $ % ).[/red]")

    # Strength levels
    if not any([length_error, lowercase_error, uppercase_error, digit_error, special_error]):
        strength = "[bold green]üîê Strong password![/bold green]"
    elif len(comments) <= 2:
        strength = "[yellow] Medium strength password.[/yellow]"
    else:
        strength = "[bold red]‚ùå Weak password.[/bold red]"

    # Build final panel text
    analysis = f"[bold cyan]Password Entered:[/bold cyan] [white]{password}[/white]\n\n"
    analysis += f"[bold magenta]Analysis Result:[/bold magenta]\n{strength}\n"
    
    if comments:
        analysis += "\n" + "\n".join(comments)

    # Show in one Panel
    console.print(Panel.fit(analysis, title="[ Password Strength Checker ]", style="bold blue"))


